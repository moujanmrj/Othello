/**
 * this class is for the disks in the board
 * it holds their color
 *
 * @author Moujan Mirjalili
 * @version 2020
 */
public class Disk
{
    //this field shows the color of the othello disks
    private String color;

    /**
     * this method returns the color of the disks
     *
     * @return color of the othello disks
     */
    public String getColor() { return color; }

    /**
     * this method sets the color of the disks
     *
     * @param color color of the othello disks
     */
    public void setColor(String color) { this.color = color; }
}
